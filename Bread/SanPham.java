public class SanPham {
    private String ten;
    private int gia;

    // Constructor
    public SanPham(String ten, int gia) {
        this.ten = ten;
        this.gia = gia;
    }

    // Getter
    public String getTen() {
        return ten;
    }

    public int getGia() {
        return gia;
    }
}
